<?php
    /* looping

        for
        while
        do while
        foreach : untuk array\

    */
    for($i = 0; $i < 5 ; $i++){
        echo "looping dengan for<br>";
    }

    $ii = 0;
    while($ii < 5){
        echo "looping dengan while<br>";
        $ii++;
    }
    $ii = 0;
    do {
        echo "looping dengan do while<br>";
        $ii++;
    }while($ii < 5)
?>
